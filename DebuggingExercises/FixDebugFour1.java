// This program assigns values to two variables
// and performs mathematical operations with them
public class FixDebugFour1
{
   public static void main(String args[])
   {
      int x = 5;
      int y = 8;
      int a = x+y;
      System.out.println("adding " + a);

      int z = 19;
      int b = z-y;
      System.out.println("subtracting "  + b);

      int c = z/x;
      System.out.println("dividing " + c);

      int d = x*z;
      System.out.println("multiplying " + d);
   }
}